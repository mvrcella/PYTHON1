n1 = int(input('Informe um número: '))
n2 = int(input('Informe outro número: '))

print(n1, '==', n2, '->', n1 == n2) 
# /\ Informa se os números são iguais

print(n1, '!=', n2, '->', n1 != n2)
# /\ Informa se os números são diferentes

print(n1, '<', n2, '->', n1 < n2)
# /\ Informa se o primeiro número é menor que o segundo

print(n1, '<=', n2, '->', n1 <= n2)
# /\ Informa se o primeiro número é menor ou igual ao segundo 

print(n1, '>', n2, '->', n1 > n2)
# /\ Informa se o primeiro número é maior que o segundo

print(n1, '>=', n2, '->', n1 >= n2)
# /\ Informa se o primeiro número é maior ou igual ao segundo
